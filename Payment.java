public class Payment {
    
    //instance variables
    private int totalPrice;

    // constructors
    public Payment(int totalPrice) {
        this.totalPrice = totalPrice;
    }

    // getters and setters
    public int getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(int totalPrice) {
        this.totalPrice = totalPrice;
    }

}
